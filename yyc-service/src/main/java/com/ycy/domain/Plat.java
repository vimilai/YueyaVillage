package com.ycy.domain;

public class Plat {
		private long plat_id;
		private String plat_name;
		private long plat_person;
		private long plat_point;
		public long getPlat_id() {
			return plat_id;
		}
		public void setPlat_id(long plat_id) {
			this.plat_id = plat_id;
		}
		public String getPlat_name() {
			return plat_name;
		}
		public void setPlat_name(String plat_name) {
			this.plat_name = plat_name;
		}
		public long getPlat_person() {
			return plat_person;
		}
		public void setPlat_person(long plat_person) {
			this.plat_person = plat_person;
		}
		public long getPlat_point() {
			return plat_point;
		}
		public void setPlat_point(long plat_point) {
			this.plat_point = plat_point;
		}
		
		
}
